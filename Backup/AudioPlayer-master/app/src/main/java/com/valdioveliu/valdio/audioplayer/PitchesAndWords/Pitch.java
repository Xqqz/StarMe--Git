public class Pitch{

    float occuranceTime;
    float pitch;


    public void setOccuranceTime(float occuranceTime) {
        this.occuranceTime = occuranceTime;
    }

    public float getOccuranceTime() {
        return occuranceTime;
    }

    public void setPitch(float pitch) {
        this.pitch = pitch;
    }

    public float getPitch() {
        return pitch;
    }
}
